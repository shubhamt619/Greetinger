/**
 * Greetinger Class
 * @class
 */
class Greetinger {
    /**
     * sayHello method
     * @returns {string} Greeting
     */
    sayHello() {
        return "Hello, world!";
    }

    /**
     * sayHi method
     * @returns {string} Greeting
     */
    sayHi() {
        return "Hi, world!";
    }
}

module.exports = Greetinger;
